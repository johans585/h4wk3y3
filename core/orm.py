"""
Argus V2 — SQLAlchemy 2.0 ORM models.

Single source of truth for the Argus schema. Alembic migrations are
autogenerated from this module ; production runs `alembic upgrade head`
and tests rely on `Base.metadata.create_all(engine)` for ad-hoc setup.

Tables:
    scans          — one row per scan invocation
    subdomains     — UNIQUE(domain, subdomain), first_seen tracker
    live_hosts     — UNIQUE(url), first_seen + tech + diff cols (1.2)
    findings       — fingerprint dedup + first/last_seen_scan_id (1.2)
    dashboard_runs — UI-launched scan processes (PID tracking)
    users          — auth (super-admin/admin/user roles)
    audit_log      — sensitive-action append log
    organisations  — BBP programme container (Étape 2.1)
    targets        — apex ↔ organisation (Étape 2.1)
"""
