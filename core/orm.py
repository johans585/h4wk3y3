# ─────────────────────────────────────────────────────────────────────
# Module artefacts (Étape 0006) — structured per-module output in Postgres.
# ─────────────────────────────────────────────────────────────────────
# Generic home for the structured, per-item module outputs that used to live
# only as JSON files under output/<domain>/ (js_secrets, js_endpoints,
# takeovers, patterns, tech, email_security, …). Promoting them to the DB
# gives the dashboard a single source of truth, plus diff/history/scoping for
# free — the same wins findings/live_hosts already get.
#
# Design:
#   - `kind` is the logical artefact class (e.g. 'js_secret'), `module` the
#     producer ('m11'). Together with `domain` they form the query identity:
#     the dashboard fetches `WHERE domain=? AND kind=?`.
#   - `dedup_key` is a stable per-item identity *within* (domain, module, kind)
#     so re-scans upsert in place and we can diff (first/last_seen_scan_id),
#     mirroring findings.fingerprint and live_hosts.url.
#   - `data` is JSONB (not Text-JSON like the rest of the schema): the whole
#     point of this table is queryability, so we deliberately break the
#     convention to allow `data @> '{...}'` / `data->>'severity'` filters.
#     Postgres-only runtime makes this safe.
class ScanArtefact(Base):
    __tablename__ = "scan_artefacts"
    __table_args__ = (
        # Cross-scan identity: same logical item upserts in place so diff works.
        UniqueConstraint(
            "domain", "module", "kind", "dedup_key",
            name="uq_scan_artefacts_identity",
        ),
        Index("idx_scan_artefacts_domain_kind", "domain", "kind"),
        Index("idx_scan_artefacts_scan",        "scan_id"),
        Index("idx_scan_artefacts_last_seen",   "domain", "kind", "last_seen_scan_id"),
    )

    id:                  Mapped[int]           = mapped_column(BigInteger, primary_key=True, autoincrement=True)
    scan_id:             Mapped[str]           = mapped_column(Text, nullable=False)
    domain:              Mapped[str]           = mapped_column(Text, nullable=False)
    module:              Mapped[str]           = mapped_column(Text, nullable=False)   # 'm11'
    kind:                Mapped[str]           = mapped_column(Text, nullable=False)   # 'js_secret'
    dedup_key:           Mapped[str]           = mapped_column(Text, nullable=False)
    data:                Mapped[Any]           = mapped_column(JSONB, nullable=False)
    # Diff tracking — same scheme as findings / live_hosts.
    first_seen_scan_id:  Mapped[Optional[str]] = mapped_column(Text, nullable=True)
    last_seen_scan_id:   Mapped[Optional[str]] = mapped_column(Text, nullable=True)
    created_at:          Mapped[str]           = mapped_column(Text, nullable=False)
    updated_at:          Mapped[str]           = mapped_column(Text, nullable=False)
